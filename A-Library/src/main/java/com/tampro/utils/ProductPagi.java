package com.tampro.utils;

public class ProductPagi<A,B> {
	 private final A first;
	 private final B second;
	 
	 
	 
	public ProductPagi(A first, B second) {
		this.first = first;
		this.second = second;
	}
	public A getFirst() {
		return first;
	}
	public B getSecond() {
		return second;
	}


	   
	

}
